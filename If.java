// this program is an 'if and else statement' written in Java


// a public class called 'Main'
public class Main{
    public static void main(String[]args) {
// integer number is 10
int number = 10;
// if 10 is greater than 5
if (number > 5)
// print, 10 is greater than 5
    System.out.println("10 is greater than 5");
// or else, it will print, 10 is not greater than 5       
else
    System.out.println("10 is not greater than 5");
   }
}
